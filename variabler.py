# Dette programmet ser på å summere heltall, printe forskjellige typer og å summere stringer.

numberOne = 24
numberTwo = 48 # Oppretter variablene med hver sin heltallsverdi.
print(numberOne)
print(numberTwo)

sumNumbers = numberOne + numberTwo # Summerer de to variablene
print("Sum:" , sumNumbers) # Printer ut stringen "Sum:" og summen av variablene på samme linje.

wordOne = "Mickey"
wordTwo = "Mouse" # Oppretter to variabler med hver sin string-verdi
sammen = wordOne + wordTwo # Slår sammen verdiene i de to variablene (uten mellomrom) og legger den nye stringen inn i en ny variabel.
print(sammen) # Printer ut den nye variabelen.

sammen = wordOne + " " + wordTwo # Slår sammen de to variablene, denne gangen med et mellomrom mellom dem.
print(sammen) # Printer ut den nye variabelen.
